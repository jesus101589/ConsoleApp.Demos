// WriteLine is a method that takes a block of text and prints it to the console followed by a new line.
Console.WriteLine("Hello, World! My name is Jesus Salinas");
Console.WriteLine("I am from the USA, and I am a software engineer!");
Console.WriteLine("Anything you want to say");

// Write is a method that takes a block of text and prints it to the console without adding a new line.
// It does not break the line after it prints the text.
Console.Write("Line 1");
Console.Write("Line 2");